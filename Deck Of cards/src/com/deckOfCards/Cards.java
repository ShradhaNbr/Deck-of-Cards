package com.deckOfCards;

public class Cards {
private String suit;
private String rank;
public Cards(String suit, String rank) {
	this.suit = suit;
	this.rank = rank;
}
public String getSuit() {
	return suit;
}
public void setsuit() {
	this.setsuit();
}
public String getrank() {
	return rank;
}
public void setrank() {
	this.setrank();
}


}
